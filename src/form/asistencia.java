    private void inicio() {
        getIcono(BtnBuscar);
        getIcono(BtnBuscar1);
        foto.setSize(104, 130);
        getIcono(foto);
        try {
            aO.obtenerLista(listaAfiliados);
            if (listaAfiliados.size() > 0) {
                tomarLista();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void tomarLista() {
        txtCed.setText(listaAfiliados.get(index).getCedula());
        txtNombre.setText(listaAfiliados.get(index).getNombre());
        txtApe.setText(listaAfiliados.get(index).getApellido());
    }

    private Icon getIcono(JLabel lb) {
        ImageIcon image = new ImageIcon("imagenes/" + lb.getName() + ".png");
        Icon icono = new ImageIcon(image.getImage().getScaledInstance(lb.getWidth(), lb.getHeight(), Image.SCALE_REPLICATE));
        lb.setIcon(icono);
        return icono;
    }

    private Icon getIcono(JButton btn) {
        btn.setSize(50, 50);
        ImageIcon image = new ImageIcon("imagenes/" + btn.getName() + ".png");
        Icon icono = new ImageIcon(image.getImage().getScaledInstance(btn.getWidth() - 1, btn.getHeight() - 2, Image.SCALE_REPLICATE));
        btn.setIcon(icono);
        btn.setHorizontalTextPosition(SwingConstants.CENTER);
        btn.setVerticalTextPosition(SwingConstants.BOTTOM);
        return icono;
    }

