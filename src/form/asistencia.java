    private void BtnBuscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscar1ActionPerformed
        index++;
        if (listaAfiliados.size() > 0 && index < listaAfiliados.size()) {
            if (index == 3) {
                JOptionPane.showMessageDialog(this, "Error no se puede conectar con el origen de datos", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            tomarLista();
        } else {
            JOptionPane.showMessageDialog(this, "Asistencia registrada", "Listo", JOptionPane.INFORMATION_MESSAGE);
            ventana.setTitle("Sistema de Gestión de Cobranza de la Organización el Rosario");
            this.setVisible(false);
        }
    }//GEN-LAST:event_BtnBuscar1ActionPerformed

    private String getfechaActual() {
        String fecha;
        Date actual = new Date();
        DateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        fecha = formato.format(actual);
        return fecha;
    }
